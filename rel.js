let a = 10;
let b = 5;

// Perform comparisons and store the results
let greater = a > b;    // true
let greaterOrEqual = a >= b; // true
let less = a < b;       // false
let lessOrEqual = a <= b; // false
let equal = a == b;     // false
let notEqual = a != b;  // true

// Output the results
console.log(`a > b: ${greater}`);
console.log(`a >= b: ${greaterOrEqual}`);
console.log(`a < b: ${less}`);
console.log(`a <= b: ${lessOrEqual}`);
console.log(`a == b: ${equal}`);
console.log(`a != b: ${notEqual}`);
